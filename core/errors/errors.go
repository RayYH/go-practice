package errors

import (
	"fmt"
)

////////////////////////////////////////////////////////////////////////////////////////////////////////////

// Go 没有像 Java 和 .NET 那样的 try/catch 异常机制，取而代之的是 defer-panic-and-recover 机制。
// Go 通过在函数和方法中返回错误对象作为它们的唯一或最后一个返回值来处理普通错误。

////////////////////////////////////////////////////////////////////////////////////////////////////////////

// MyError custom error struct
type MyError struct {
	when string
	what string
}

func (e *MyError) Error() string {
	return fmt.Sprintf("at %s, %s", e.when, e.what)
}
